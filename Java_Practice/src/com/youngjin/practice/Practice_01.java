package com.youngjin.practice;

public class Practice_01 {

	public static void main(String[] args) {
		
		//두 정수 33과 2를 사용하여 4칙연산하여 Console에 출력
		
		System.out.println("==============");
		System.out.printf("%d + %d = %d\n" , 33 , 2, 33 + 2);
		System.out.printf("%d - %d = %d\n" , 33 , 2, 33 - 2);
		System.out.printf("%d x %d = %d\n" , 33 , 2, 33 * 2);
		System.out.printf("%d / %d = %d\n" , 33 , 2, 33 / 2);
		System.out.printf("%d d %d = %d\n" , 33 , 2, 33 % 2);
		System.out.println("==============");
	}
	
}
