package com.youngjin.practice;

public class Practice_03 {

	public static void main(String[] args) {
		int num1 = 0;
		int num2 = 0;
		
		num1 = 33;
		num2 = 55;
		
		System.out.printf("%d + %d = %d\n",
				num1, num2, num1 + num2);
		System.out.printf("%d - %d = %d\n",
				num2, num1, num2 - num1);
		System.out.printf("%d x %d = %d\n",
				num1, num2, num1 * num2);
		System.out.printf("%d / %d = %d",
				num2, num1, num2 / num1);
		
	}
	
}
